package internas;

public class Usuario {
	private String id;
	private String nome;
	private String email;
	private String celular;
	private String classe;

	public Usuario(String id, String nome, String email, String celular, String classe) {
		this.id = id;
		this.nome = nome;
		this.email = email;
		this.celular = celular;
		this.classe = classe;
	}

}
